						<h2>Guiding Principles of Pakistan’s Foreign Policy

						</h2>
						<div class="presidentPara"><p>

							<img src="img/pakitaniFlag.jpg" alt="" class="center-block">
							<br>
							After taking oath of the office, Prime Minister Muhammad Nawaz Sharif has addressed a message for all Heads of Pakistan Missions abroad. The Prime Minister has stated that the people of Pakistan have pronounced themselves clearly on the future direction that our country should take. They have also been abundantly clear in underscoring their priorities. <br><br>

							The Prime Minister said that the government will be guided by the wisdom of the people, the vision of Quaid-e-Azam Muhammad Ali Jinnah and the desire to see Pakistan acquire its rightful place in the comity of nations. Pakistan’s size and innate strengths should enable it to have a key role on the global scene. Heads of Missions and Embassies, that are our first line of defence and our primary interface with the outside world, have important roles to play to achieve this objective. <br><br>

							The Prime Minister has also provided an outline of the priorities of the government on Foreign Policy to serve as basic guidelines for our Embassies/Missions abroad. The Prime Minister said that national interests shall drive our foreign policy in all circumstances, and there is an across-the-board consensus on this. He stressed the need to create an external environment in which a democratic and progressive Pakistan will be engaged with the outside world. This will be shaped by Quaid-e-Azam’s vision of peace within and peace without.<br><br>

							The Prime Minister noted that we have a large youthful population whose potential needs to be harnessed for the development of Pakistan. The youth, especially those who voted in the May 11 elections, demand it.<br><br>

							The Prime Minister further noted that the core of internal strength lies in sustained economic growth, resulting in the overall well-being, progress and prosperity of the people of Pakistan. Our Diplomatic Missions have a pivotal role in advancing this central objective of the national agenda.<br><br>

							The Prime Minister directed our Missions Abroad to aggressively pursue economic diplomacy - promotion of Pakistan’s interests in the realms of trade, foreign investment, and economic cooperation.<br><br>

							Effective economic diplomacy would require synergistic relationship between the Diplomatic Missions and the relevant stakeholders in Pakistan including Government Ministries as well as the business community at large. The Economic Ministries/Departments – i.e. Commerce, Finance, EAD and BOI must provide the requisite information and backup support needed by the Missions to undertake practical steps for facilitating traders and current and prospective investors at both ends.<br><br>

							The Prime Minister expressed government’s firm commitment to address the energy crisis facing Pakistan in the shortest possible time. He encouraged the Head of Missions to identify potential bilateral and multilateral partners to help to address the energy crisis and formulate tangible project proposals in the traditional and renewable energy sectors.<br><br>

							Stressing the importance of Science, Technology and education, the Prime Minister directed the Missions Abroad to actively reach out to institutions and the private sector in search of technologies and know-how and develop institutional linkages between our research and educational centres and those abroad.<br><br>

							The Prime Minister noted that the immediate attention of government will be focused on our neighbours. Unless the region is peaceful, our efforts for growth and development will not meet with success.<br><br>

							The Prime Minister stressed the importance of developing regional consensus on supporting a stable government and peace in Afghanistan. He affirmed that a policy of an Afghan-led, Afghan-owned process of peace and reconciliation will be pursued.<br><br>

							With India, the Prime Minister stressed the need to progressively pursue normalcy in our bilateral relations, while actively seeking solutions for all outstanding issues, including Jammu and Kashmir.<br><br>

							The Prime Minister expressed the desire to deepen our relations with our friends and brothers in the Middle East, East Asia and elsewhere. Saudi Arabia, Turkey and Iran are friendly countries with which we shall continue to pursue close cooperative relations.<br><br> 

							On relations with the US, the Prime Minister noted that the two countries have many areas of common interest. We will build on convergences and wherever there is divergence we will work to try to minimize and remove such divergence.<br><br>

							With China, which has always been a great friend and a significant economic partner, Pakistan will strengthen this valuable and much appreciated strategic relationship.<br><br>

							With Russia, which is an important partner and regional power, we shall continue to enhance ties.<br><br>

							Despite its recent economic troubles, Europe continues to remain an important player on the global scene and is our largest trading partner. We will continue to engage with European countries, both bilaterally with individual countries, as well as multilaterally with the European Union.<br><br>

							On terrorism, the Prime Minister said that the government will develop a comprehensive strategy and a national consensus in consultation will all political parties, security forces, media and the civil society in dealing with this phenomenon. At the same time, it is imperative to address the external dimension of terrorism. This would require plugging the sources of external funding of various extremists groups besides seeking cooperation of regional and global community.<br><br>

							The Prime Minister directed that one of the main task of our Embassies is  to look after the increasingly large number of Pakistanis living abroad who are an asset for Pakistan. He directed the Embassies to provide efficient consular services and protect the interests of Pakistanis abroad.<br><br>

							The Prime Minister directed the Embassies to make concerted efforts to project Pakistan’s positive image abroad, its historical heritage, music, art, culture and cuisine.<br><br>

							The Prime Minister stressed the need to work together for a progressive and developed Pakistan.<br><br>



