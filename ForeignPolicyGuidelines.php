							Quaid-e-Azam Muhammad Ali Jinnah, the Founder of Pakistan and its first Governor General, in a broadcast talk to the people of the USA in February 1948, outlined the following goals of Pakistan’s foreign policy: <br><br>

							“Our foreign policy is one of friendliness and goodwill towards all the nations of the world. We do not cherish aggressive designs against any country or nation. We believe in the principle of honesty and fair play in national and international dealings and are prepared to make our utmost contribution to the promotion of peace and prosperity among the nations of the world. Pakistan will never be found lacking in extending its material and moral support to the oppressed and suppressed peoples of the world, and in upholding the principles of the United Nations Charter.” <br><br>

							The Constitution of Pakistan also lays down guidelines for the conduct of foreign policy of the country. Article 40 of the constitution provides that: <br><br>

							“The State shall endeavour to preserve and strengthen fraternal relations among Muslim countries based on Islamic unity, support the common interests of the peoples of Asia, Africa and Latin America, promote international peace and security, foster goodwill and friendly relations among all nations and encourage the settlement of international disputes by peaceful means.” <br><br>

							The foreign policy of Pakistan is primarily directed to the pursuit of national goals of seeking peace and stability through international cooperation. Special emphasis is laid on economic diplomacy to take advantages offered by the process of globalization as also to face challenges of the 21st century. Our foreign policy is also geared to project the image of the country as a dynamic and moderate society. <br><br>

							The foreign policy of Pakistan seeks to promote the internationally recognized norms of interstate relations, i.e. respect for sovereignty and territorial integrity of all States, non-interference in the internal affairs of other State; non-aggression and peaceful settlement of disputes. Pakistan has therefore always sought to develop friendly and cordial relations with all countries of the world. <br><br>

